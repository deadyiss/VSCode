for i in range(90, 64, -1):
    print(chr(i), ' ', end="")
    
#Fungsi range(start, stop, step) menghasilkan bilangan dalam rentang tertentu.
#Start: 90 (ASCII untuk 'Z').
#Stop: 64 (nilai sebelum 65, karena range tidak menyertakan nilai stop).
#Step: -1 (mengurangi 1 setiap iterasi untuk berjalan mundur).